<script>
export default {
    data() {
        return {
            movie: null,
            showNoti: "",
            status: '',
        }
    },
    async fetch() {
        try {
            this.id = this.$route.params.id;
            let url = `http://localhost:8080/api/movie/` + this.id;
            await this.$axios.get(url).then((response) => {
                this.movie = response.data;
            });
        }
        catch (error) {
            console.log(error);
        }
    },
    computed: {
        formattedReleaseDate: {
            get() {
                // Lấy giá trị releaseDate từ movie object
                const releaseDate = this.movie.releaseDate;
                // Tạo một instance của Date từ releaseDate
                const date = new Date(releaseDate);
                // Format ngày thành yyyy-MM-dd
                const formattedDate = date.toISOString().split("T")[0];
                // Trả về giá trị đã format
                return formattedDate;
            },
            set(value) {
                // Khi người dùng thay đổi trường input
                // Parse giá trị ngày từ yyyy-MM-dd thành định dạng ISO
                const parsedDate = new Date(`${value}T00:00:00.000Z`);
                // Gán giá trị parsedDate cho movie.releaseDate
                this.movie.releaseDate = parsedDate.toISOString();
            },
        },
    },
    methods: {
        async update() {
            try {
                await this.$axios.put(`http://localhost:8080/api/movie/update`, this.movie).then((response) => {
                    this.movie = response.data;
                    this.status = "thành công";
                    this.showNoti = "Ok";
                    setTimeout(() => {
                        this.showNoti = "";
                        // this.$router.push(`/movie/${this.movie.id}`);
                    }, 1500);
                })
            } catch (error) {
                console.log(error);
            }
        }
    }
}
